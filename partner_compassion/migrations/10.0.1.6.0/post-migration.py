from openupgradelib import openupgrade


@openupgrade.migrate(use_env=True)
def migrate(env, version):
    if not version:
        return

    env.cr.execute("""
UPDATE ambassador_details
SET state = 'active'
FROM correspondence
WHERE ambassador_details.partner_id = correspondence.translator_id;
    """)
