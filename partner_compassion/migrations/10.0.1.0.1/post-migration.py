from openupgradelib import openupgrade


@openupgrade.migrate(use_env=True)
def migrate(env, version):
    if not version:
        return

    # FIX sponsorship count
    env['res.partner'].search([
        ('number_sponsorships', '!=', 0)]).update_sponsorship_number()
